package conditional_statements;

public class IfNumberIsZero {

	public static void main(String[] args) {

		int n=-1;
		
		if(n<0)
		{
			System.out.println("Number is Negative");
		}
		else if(n>0)
		{
			System.out.println("Number is positive");
		}
		else
		{
			System.out.println("Number is 0");
		}
	}

}
