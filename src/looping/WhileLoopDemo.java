
package looping;

public class WhileLoopDemo {

	public static void main(String[] args) {
		int i = 1; // initialization
		while (i <= 10) // condition
		{
			System.out.println(i);
			i++; // increment

		}

	}

}

// There are 3 looping statements: while, do while and for
// They are used to do a certain task until the condition is satisfied. 
// ex.print the numbers 1 to 10: While i<=10, print the i
// Every looping statements needs to satisfy 3 conditions: initialization, condition, increment